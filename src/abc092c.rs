use std::cmp::{max,min};
fn main(){
    input!{
        n:usize,
        mut tmp:[i64;n],
    }
    let mut an = vec![0];
    an.append(&mut tmp);
    an.push(0);
    let n = n + 2;
    let mut sum = 0;
    for j in 0..n-1 {
        sum += (an[j+1]-an[j]).abs();
    }
    for j in 0..n-2 {
        let div1 = an[j+1] - an[j];
        let div2 = an[j+2] - an[j+1];
        let div3 = an[j+2] - an[j];
        
        println!("{}",sum+div3.abs()-div2.abs()-div1.abs());
    }
}