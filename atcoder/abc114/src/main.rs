fn recv(num:i64,n:&i64) -> i64 {
    let v = vec![3,5,7];
    if num > *n {return 0}
    let mut ans = if v.iter().all(|&j| num.to_string().chars().filter(|&c| c == std::char::from_digit(j as u32 ,10).unwrap()).count() != 0) {1} else {0};
    for &a in &v {
        let x = num*10 + a;
        ans +=  recv(x,n);
    }
    ans
} 